-- View que agrega dados importados no mesmo formato de matriz_orc_v1_unidade
-- Regra de nível: 'G', 'GRAD' ou contém 'gradua' → Graduação; demais → Pós
-- Situações ignoradas: cancelada, inativa, desativada
-- Classificação por nivel_ensino do CSV:
--   'Ensino Superior' → CH Grad + Alunos Grad
--   'Stricto Sensu'   → CH Pós  + Alunos Pós
-- Sem filtro de situacao_turma — todos os registros são contabilizados
DROP VIEW IF EXISTS utfprct.vw_matriz_orc_v1_importado CASCADE;
CREATE VIEW utfprct.vw_matriz_orc_v1_importado AS
SELECT
  i.configuracao_id,
  u.id   AS unidade_id,
  u.sigla,
  u.nome,
  u.tipo,
  SUM(CASE WHEN i.nivel_ensino = 'Ensino Superior' THEN i.cht              ELSE 0 END) AS carga_horaria_graduacao,
  SUM(CASE WHEN i.nivel_ensino = 'Stricto Sensu'   THEN i.cht              ELSE 0 END) AS carga_horaria_pos,
  SUM(CASE WHEN i.nivel_ensino = 'Ensino Superior' THEN i.quantidade_alunos ELSE 0 END) AS alunos_graduacao,
  SUM(CASE WHEN i.nivel_ensino = 'Stricto Sensu'   THEN i.quantidade_alunos ELSE 0 END) AS alunos_pos,
  COALESCE(MAX(man.peso_unidade), 1.0)::numeric AS peso_unidade
FROM utfprct.matriz_orc_v1_importacao i
JOIN utfprct.matriz_orc_unidades u
  ON UPPER(TRIM(u.sigla)) = UPPER(TRIM(i.departamento))
LEFT JOIN utfprct.matriz_orc_v1_unidade man
  ON man.unidade_id = u.id AND man.configuracao_id = i.configuracao_id
GROUP BY i.configuracao_id, u.id, u.sigla, u.nome, u.tipo;

-- View: departamentos importados que não têm correspondência em unidades cadastradas
DROP VIEW IF EXISTS utfprct.vw_v1_departamentos_nao_reconhecidos;
CREATE VIEW utfprct.vw_v1_departamentos_nao_reconhecidos AS
SELECT
  i.configuracao_id,
  c.ano,
  i.departamento,
  COUNT(*) AS total_linhas,
  SUM(i.quantidade_alunos) AS total_alunos,
  SUM(i.cht) AS total_cht
FROM utfprct.matriz_orc_v1_importacao i
JOIN utfprct.matriz_orc_configuracao_base c ON c.id = i.configuracao_id
WHERE NOT EXISTS (
  SELECT 1 FROM utfprct.matriz_orc_unidades u
  WHERE UPPER(TRIM(u.sigla)) = UPPER(TRIM(i.departamento))
)
GROUP BY i.configuracao_id, c.ano, i.departamento
ORDER BY c.ano DESC, i.departamento;

-- Remove coluna situacao_turma (não utilizada na view)
ALTER TABLE utfprct.matriz_orc_v1_importacao
