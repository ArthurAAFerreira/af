-- Atualiza vw_matriz_orc_resultado para respeitar v1_modo:
--   MANUAL    → lê de matriz_orc_v1_unidade (comportamento original)
--   IMPORTADO → lê CH/alunos de vw_matriz_orc_v1_importado + peso_unidade de matriz_orc_v1_unidade
CREATE OR REPLACE VIEW utfprct.vw_matriz_orc_resultado AS
WITH cfg AS (
  SELECT * FROM utfprct.matriz_orc_configuracao_base WHERE ativo = true ORDER BY id DESC LIMIT 1
),
v1_fonte AS (
  SELECT
    imp.unidade_id,
    imp.configuracao_id,
    imp.carga_horaria_graduacao,
    imp.carga_horaria_pos,
    imp.alunos_graduacao,
    imp.alunos_pos,
    COALESCE(man.peso_unidade, 1.0) AS peso_unidade
  FROM utfprct.vw_matriz_orc_v1_importado imp
  JOIN cfg ON imp.configuracao_id = cfg.id AND cfg.v1_modo = 'IMPORTADO'
  LEFT JOIN utfprct.matriz_orc_v1_unidade man
    ON man.unidade_id = imp.unidade_id AND man.configuracao_id = cfg.id
  UNION ALL
  SELECT
    v.unidade_id,
    v.configuracao_id,
    v.carga_horaria_graduacao,
    v.carga_horaria_pos,
    v.alunos_graduacao,
    v.alunos_pos,
    v.peso_unidade
  FROM utfprct.matriz_orc_v1_unidade v
  JOIN cfg ON v.configuracao_id = cfg.id AND cfg.v1_modo = 'MANUAL'
),
v1 AS (
  SELECT
    f.unidade_id,
    f.configuracao_id,
    (
      (f.carga_horaria_graduacao * f.alunos_graduacao * cfg.v1_peso_graduacao / 100.0)
      + (f.carga_horaria_pos * f.alunos_pos * cfg.v1_peso_pos_graduacao / 100.0)
    ) * f.peso_unidade AS score
  FROM v1_fonte f, cfg
),
v2 AS (
  SELECT
    v.unidade_id,
    (
      (v.docentes_20h * cfg.v2_peso_20h + v.docentes_40h * cfg.v2_peso_40h + v.docentes_de * cfg.v2_peso_de) * cfg.v2_peso_docentes / 100.0
      + (v.taes_parcial * cfg.v2_peso_funcao_parcial + v.taes_integral * cfg.v2_peso_funcao_integral) * cfg.v2_peso_taes / 100.0
      + (v.docentes_funcao_parcial * cfg.v2_peso_funcao_parcial + v.docentes_funcao_integral * cfg.v2_peso_funcao_integral) * cfg.v2_peso_docentes / 100.0
    ) AS score
  FROM utfprct.matriz_orc_v2_unidade v, cfg WHERE v.configuracao_id = cfg.id
),
v3 AS (
  SELECT
    v.unidade_id,
    (
      v.docentes_graduacao * cfg.v3_peso_graduacao
      + v.docentes_especializacao * cfg.v3_peso_especializacao
      + v.docentes_mestrado * cfg.v3_peso_mestrado
      + v.docentes_doutorado * cfg.v3_peso_doutorado
    ) AS score
  FROM utfprct.matriz_orc_v3_unidade v, cfg WHERE v.configuracao_id = cfg.id
),
v4_raw AS (
  SELECT
    e.unidade_id,
    SUM(COALESCE(a.peso,0) * COALESCE(t.peso,0)) AS score
  FROM utfprct.matriz_orc_estruturas e
  JOIN utfprct.matriz_orc_estrutura_tipos t ON t.id = e.tipo_id
  JOIN utfprct.matriz_orc_estrutura_selecao sel ON sel.estrutura_id = e.id
  JOIN utfprct.matriz_orc_estrutura_atributos a ON a.id = sel.atributo_id
  JOIN cfg ON e.configuracao_id = cfg.id AND e.ativo = true
  GROUP BY e.unidade_id
),
combined AS (
  SELECT
    u.id AS unidade_id,
    u.sigla,
    u.nome,
    u.tipo,
    cfg.id AS configuracao_id,
    cfg.peso_v1, cfg.peso_v2, cfg.peso_v3, cfg.peso_v4,
    COALESCE(v1.score, 0) AS score_v1,
    COALESCE(v2.score, 0) AS score_v2,
    COALESCE(v3.score, 0) AS score_v3,
    COALESCE(v4_raw.score, 0) AS score_v4
  FROM utfprct.matriz_orc_unidades u
  CROSS JOIN cfg
  LEFT JOIN v1 ON v1.unidade_id = u.id
  LEFT JOIN v2 ON v2.unidade_id = u.id
  LEFT JOIN v3 ON v3.unidade_id = u.id
  LEFT JOIN v4_raw ON v4_raw.unidade_id = u.id
  WHERE u.ativo = true
),
totais AS (
  SELECT
    configuracao_id,
    SUM(score_v1) AS total_v1,
    SUM(score_v2) AS total_v2,
    SUM(score_v3) AS total_v3,
    SUM(score_v4) AS total_v4
  FROM combined GROUP BY configuracao_id
),
indice AS (
  SELECT
    c.*,
    t.total_v1, t.total_v2, t.total_v3, t.total_v4,
    CASE WHEN t.total_v1 > 0 THEN c.score_v1 / t.total_v1 ELSE 0 END AS idx_v1,
    CASE WHEN t.total_v2 > 0 THEN c.score_v2 / t.total_v2 ELSE 0 END AS idx_v2,
    CASE WHEN t.total_v3 > 0 THEN c.score_v3 / t.total_v3 ELSE 0 END AS idx_v3,
    CASE WHEN t.total_v4 > 0 THEN c.score_v4 / t.total_v4 ELSE 0 END AS idx_v4,
    NULLIF(
      CASE WHEN t.total_v1 > 0 THEN c.peso_v1 ELSE 0 END
      + CASE WHEN t.total_v2 > 0 THEN c.peso_v2 ELSE 0 END
      + CASE WHEN t.total_v3 > 0 THEN c.peso_v3 ELSE 0 END
      + CASE WHEN t.total_v4 > 0 THEN c.peso_v4 ELSE 0 END, 0
    ) AS active_w_sum
  FROM combined c JOIN totais t ON t.configuracao_id = c.configuracao_id
)
SELECT
  i.unidade_id,
  i.sigla,
  i.nome,
  i.tipo,
  i.score_v1, i.score_v2, i.score_v3, i.score_v4,
  COALESCE(
    ( CASE WHEN i.total_v1 > 0 THEN i.idx_v1 * i.peso_v1 ELSE 0 END
    + CASE WHEN i.total_v2 > 0 THEN i.idx_v2 * i.peso_v2 ELSE 0 END
    + CASE WHEN i.total_v3 > 0 THEN i.idx_v3 * i.peso_v3 ELSE 0 END
    + CASE WHEN i.total_v4 > 0 THEN i.idx_v4 * i.peso_v4 ELSE 0 END
    ) / i.active_w_sum, 0
  ) AS indice_composto,
  cfg.recurso_bruto,
  cfg.recurso_liquido,
  cfg.pct_total,
  cfg.recurso_liquido * cfg.pct_total / 100.0 AS valor_base,
  COALESCE(
    ( CASE WHEN i.total_v1 > 0 THEN i.idx_v1 * i.peso_v1 ELSE 0 END
    + CASE WHEN i.total_v2 > 0 THEN i.idx_v2 * i.peso_v2 ELSE 0 END
    + CASE WHEN i.total_v3 > 0 THEN i.idx_v3 * i.peso_v3 ELSE 0 END
    + CASE WHEN i.total_v4 > 0 THEN i.idx_v4 * i.peso_v4 ELSE 0 END
    ) / i.active_w_sum, 0
  ) * cfg.recurso_liquido * cfg.pct_total / 100.0 AS valor_estimado
FROM indice i, cfg;

