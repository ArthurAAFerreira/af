create schema if not exists utfprct;

create or replace function utfprct.set_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

create table if not exists utfprct.matriz_orc_departamentos (
  codigo text primary key,
  categoria text not null default 'DEPARTAMENTO',
  ativo boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

drop trigger if exists trg_matriz_orc_departamentos_updated_at on utfprct.matriz_orc_departamentos;
create trigger trg_matriz_orc_departamentos_updated_at
before update on utfprct.matriz_orc_departamentos
for each row execute function utfprct.set_updated_at();

